package xymz.model;

public class WBCount {

	private String wbName;
	private Long count;
	private Long xsCount;
	private Long xxCount;
	public String getWbName() {
		return wbName;
	}
	public void setWbName(String wbName) {
		this.wbName = wbName;
	}
	public Long getCount() {
		return count;
	}
	public void setCount(Long count) {
		this.count = count;
	}
	public Long getXsCount() {
		return xsCount;
	}
	public void setXsCount(Long xsCount) {
		this.xsCount = xsCount;
	}
	public Long getXxCount() {
		return xxCount;
	}
	public void setXxCount(Long xxCount) {
		this.xxCount = xxCount;
	}
	
	
}
